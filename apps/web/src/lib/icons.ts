/** Ordered list of capability icons for picker UI. */
export const CAPABILITY_ICON_LIST: { name: string; Icon: LucideIcon }[] = Object.entries(CAPABILITY_ICON_MAP).map(
  ([name, Icon]) => ({ name, Icon }),
);

/** Resolve a capability icon name to a Lucide component. Falls back to Sprout. */
export function resolveCapabilityIcon(name: string): LucideIcon {
  return CAPABILITY_ICON_MAP[name] || Sprout;
}
