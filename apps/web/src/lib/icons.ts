// ─── Navigation Icons ────────────────────────────────────

export const NAV_ICONS: Record<string, LucideIcon> = {
  chat: MessageCircle,
  wiki: BookOpen,
  sync: RefreshCw,
};

// ─── Content Category Icons ──────────────────────────────

export const CATEGORY_ICONS: Record<string, LucideIcon> = {
  product: Package,
  plant: Sprout,
  faq: HelpCircle,
  blog: FileEdit,
  topic: Files,
};

/** Get the Lucide icon for a content category. */
export function getCategoryIcon(category: string): LucideIcon {
  return CATEGORY_ICONS[category] || FileText;
}

// ─── Context Type Icons ─────────────────────────────────

export const CONTEXT_ICONS: Record<string, LucideIcon> = {
  'wiki-detail': FileText,
  'wiki-list': BookOpen,
  chat: MessageCircle,
  history: ClipboardList,
  dashboard: LayoutDashboard,
};

/** Get the Lucide icon for a page context type. */
export function getContextIcon(type: string): LucideIcon {
  return CONTEXT_ICONS[type] || FileText;
}

